<h2>About</h2>
<p>We are Jack & Tasha, a musical duo with a spicy taste in great music. We put on open mic events and gigs around the Leicester area. Our events are inclusive and diverse bringing together the best of the local music scene.</p>

<p>Formed in 2024 from a mutual love of the open mic scene in Leicestershire, we get our audiences dancing using the power of performance. Our setlist caters to all ages and genres making sure there’s something for everyone.</p>

